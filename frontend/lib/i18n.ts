const statusLabels: Record<Language, Record<string, string>> = {
  ro: {
    Active: "Activ",
    Closed: "Închis",
    "Past due": "Restant",
  },
  en: {},
};

export function productLabel(product: string, language: Language): string {
  return productLabels[language][product] ?? product;
}

export function statusLabel(status: string, language: Language): string {
  return statusLabels[language][status] ?? status;
}
